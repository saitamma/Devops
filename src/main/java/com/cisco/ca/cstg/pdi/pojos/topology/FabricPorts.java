public class FabricPorts {

	private String fabricport;
	private String mdsport;

	/**
	 * @return the fabricport
	 */
	public String getFabricport() {
		return fabricport;
	}

	/**
	 * @param fabricport
	 *            the fabricport to set
	 */
	public void setFabricport(String fabricport) {
		this.fabricport = fabricport;
	}

	/**
	 * @return the mdsport
	 */
	public String getMdsport() {
		return mdsport;
	}

	/**
	 * @param mdsport
	 *            the mdsport to set
	 */
	public void setMdsport(String mdsport) {
		this.mdsport = mdsport;
	}

}
